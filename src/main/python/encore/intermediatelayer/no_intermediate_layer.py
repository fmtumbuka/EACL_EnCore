import encore.intermediatelayer.base_intermediate_layer as base_intermediate_layer
import torch
import typing


class NoIntermediateLayer(base_intermediate_layer.BaseIntermediateLayer):
    """This does not do any further computations on the representations."""

    def __init__(self):
        """This creates an instance of `NoIntermediateLayer`."""
        super().__init__()

    def forward(
            self,
            input_seq: torch.FloatTensor,
            mask: torch.Tensor = None,
            labels: torch.Tensor = None,
            rel_embed: bool = False
    ) -> typing.Tuple[torch.FloatTensor, typing.Any]:
        representations = None
        intermediate_loss = 0
        representations = self.retrieve_representations(input_seq, mask)
        return representations, intermediate_loss
